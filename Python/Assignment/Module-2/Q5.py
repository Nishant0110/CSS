#What is the purpose continue statement in python?
'''
    The continue statement in Python serves as a loop control statement.
    When encountered within a loop (such as a for or while loop),
    it skips the remaining code inside the loop for the current iteration and proceeds to the next iteration.
    it allows you to bypass specific parts of the loop and continue with the next iteration.
   example.
    for i in range(1, 11):
    if i == 6:
        continue
    else:
        print(i, end=" ")
    Output: 1 2 3 4 5 7 8 9 10

'''
