# What is Instantiation in terms of OOP terminology?
'''
- In Object-Oriented Programming (OOP), instantiation is the process of creating an instance of a class.
- An instance, often referred to as an object, is a concrete occurrence of any object defined by a class.
- When a class is defined, no memory is allocated until an object of that class is created.
- This creation process is known as instantiation.

1.Class Definition: A class is a blueprint or template for creating objects.
  It defines the properties (attributes) and behaviors (methods) that the objects created from the class can have.
2.Instantiation: Instantiation is the act of creating a specific object from a class.
  This is done using the class constructor, a special method that initializes the new object.
3.Object: Once instantiated, an object is a specific instance of a class, with its own unique set of attributes and methods defined by the class.
'''
